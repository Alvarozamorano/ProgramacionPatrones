    private void actualizarComboBoxMenu() throws IOException, ClassNotFoundException{
        try {
            //Obtenemos todas las comidas y bebidas disponibles de la cafeteria
            //en la que está el camarero que entra a esta ventana
            String nombre_cafeteria = usuario.getRolCamarero().getNombre_cafeteria();
            Cafeteria cafeteria = (Cafeteria) SingletonProxyServidor.getInstancia().cargar_archivo(TipoArchivo.CAFETERIA, nombre_cafeteria);
            ArrayList<ProductoCafeteria> listaProductosCafeteria = cafeteria.getProductos();
            jComboBoxPrimerPlato.removeAllItems();
            jComboBoxSegundoPlato.removeAllItems();
            jComboBoxPostre.removeAllItems();
            jComboBoxBebida.removeAllItems();
            for (ProductoCafeteria entry : listaProductosCafeteria) {
                System.out.println("clave=" + entry.getIdentificador() + ", valor=" + entry.getNombre());
                if(entry instanceof Comida){
                    jComboBoxPrimerPlato.addItem(entry.getIdentificador());
                    jComboBoxSegundoPlato.addItem(entry.getIdentificador());
                }else if(entry instanceof Postre){                  
                    jComboBoxPostre.addItem(entry.getIdentificador());
                }else if(entry instanceof Bebida){
                    jComboBoxBebida.addItem(entry.getIdentificador());
                }else{
                    //Probablemente sea un Menu, pero no utilizamos en la creación.
                    //Es decir, la creación de nuevo menús no se basa en menús
                    //ya existentes, sino en Comidas y Bebidas  ya existentes.
                }
            }
        }catch(ArrayIndexOutOfBoundsException e){
            System.out.println("\nFue del dominio del array: " + e.toString());
            JOptionPane.showMessageDialog(null,"\nFue del dominio del array: " + e.toString(),"Fallo",JOptionPane.ERROR_MESSAGE);

        }catch(NumberFormatException e){
            System.out.println("\nError en la conversión: " + e.toString());
            JOptionPane.showMessageDialog(null,"\nError en el formato del numero: " ,"Fallo",JOptionPane.ERROR_MESSAGE);

        }catch(NullPointerException e){
            System.out.println("\nVariable no definida: " + e.toString());
            JOptionPane.showMessageDialog(null,"\nVariable no definida: " + e.toString(),"Fallo",JOptionPane.ERROR_MESSAGE);

        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"\nSe ha podrucido un error: " + e.toString(),"Fallo",JOptionPane.ERROR_MESSAGE);
        }
    }

