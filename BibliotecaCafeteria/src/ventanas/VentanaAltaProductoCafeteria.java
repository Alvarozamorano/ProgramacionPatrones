    private void jButtonCrearProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCrearProductoActionPerformed
        try {
            String nombre_cafeteria = usuario.getRolCamarero().getNombre_cafeteria();
            Cafeteria cafeteria = (Cafeteria) SingletonProxyServidor.getInstancia().cargar_archivo(TipoArchivo.CAFETERIA, nombre_cafeteria);
            if(jComboBoxTipoProducto.getSelectedIndex() == 2){

                String nombre = jTextFieldNombre.getText();
                float precio = Float.valueOf(jTextFieldPrecio.getText());
                String id = jTextFieldId.getText();

                MenuBuilder menuBuilder = new MenuLibreBuilder();  
                menuBuilder.crearNuevoMenu(nombre, precio, id, cafeteria);

                DirectorBuilder directorBuilder = new DirectorBuilder();
                directorBuilder.setMenuBuilder(menuBuilder);

                //Obtenemos los ProductosCafeteria de los jComboBox
                ArrayList<ProductoCafeteria> listaProductosCafeteria = cafeteria.getProductos();

                Comida primerPlato = null;
                Comida segundoPlato = null;
                Postre postre = null;
                Bebida bebida = null;
                for (ProductoCafeteria entry : listaProductosCafeteria) {
                    if(entry instanceof Comida){
                        if(entry.getIdentificador().equals(jComboBoxPrimerPlato.getSelectedItem())){
                            primerPlato = (Comida) entry;
                        }
                        if(entry.getIdentificador().equals(jComboBoxSegundoPlato.getSelectedItem())){
                            segundoPlato = (Comida) entry;
                        }
                    }else if(entry instanceof Postre){
                        if(entry.getIdentificador().equals(jComboBoxPostre.getSelectedItem())){
                            postre = (Postre) entry;
                        }
                    }else if(entry instanceof Bebida){
                        if(entry.getIdentificador().equals(jComboBoxBebida.getSelectedItem())){
                            bebida = (Bebida) entry;
                        }
                    }else{
                        //Probablemente sea un Menu, pero no utilizamos en la creación.
                        //Es decir, la creación de nuevo menús no se basa en menús
                        //ya existentes, sino en Comidas y Bebidas  ya existentes.
                    }
                }            

                directorBuilder.crearMenu(primerPlato, segundoPlato, postre, bebida);
                Menu menu2 = directorBuilder.getMenu();
                cafeteria.añadirProducto(menu2);

                
                SingletonProxyServidor singletonProxyServidor = SingletonProxyServidor.getInstancia();
                try {
                    //Guardar la nueva Cafeteria en su carpeta.
                    singletonProxyServidor.guardar_archivo(cafeteria);
                } catch (IOException ex) {
                    Logger.getLogger(VentanaAltaProductoCafeteria.class.getName()).log(Level.SEVERE, null, ex);
                } 
            }else{
                String nombre = jTextFieldNombre.getText();
                float precio = Float.valueOf(jTextFieldPrecio.getText());
                String id = jTextFieldId.getText();
                int tipoProductoString = jComboBoxTipoProducto.getSelectedIndex();
                System.out.println(tipoProductoString);

                //Recordemos que la factoria ya introduce el Producto en el objeto Cafeteria correspondiente.
                ProductoCafeteria comida = new FactoriaProductoCafeteria().getProductoCafeteria(tipoProductoString, nombre, precio, id, cafeteria);

                SingletonProxyServidor singletonProxyServidor = SingletonProxyServidor.getInstancia();
                try {
                    //Más que guardar comidas y cafeterias, lo importante es guardar
                    //a la persona, puesto que en ella está la nueva Cafeteria.
                    singletonProxyServidor.guardar_archivo(cafeteria);
                } catch (IOException ex) {
                    Logger.getLogger(VentanaAltaProductoCafeteria.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            
            actualizarComboBoxMenu();
            JOptionPane.showMessageDialog(this,"\nProducto creado! " ,"Info",JOptionPane.INFORMATION_MESSAGE);     
        } catch (IOException | ClassNotFoundException ex) {
                Logger.getLogger(VentanaAltaProductoCafeteria.class.getName()).log(Level.SEVERE, null, ex);
        }  
    }//GEN-LAST:event_jButtonCrearProductoActionPerformed

