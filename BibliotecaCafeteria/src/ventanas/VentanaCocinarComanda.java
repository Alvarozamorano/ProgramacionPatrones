    private void jButtonCocinarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCocinarActionPerformed
        try {
            Integer index = Integer.valueOf(jTextFieldId.getText())-1;
            String nombre_cafeteria = usuario.getRolCamarero().getNombre_cafeteria();
            Cafeteria cafeteria = (Cafeteria) SingletonProxyServidor.getInstancia().cargar_archivo(TipoArchivo.CAFETERIA, nombre_cafeteria);
            
            ArrayList<Comanda> listaComandasPendientes = cafeteria.obtenerComandasPendientes();
            
            listaComandasPendientes.get(index).prepararComanda();
            listaComandasPendientes.get(index).entregarComanda();
            JOptionPane.showMessageDialog(this,"\nÂ¡Comanda cocinada y entregada! " ,"Fallo",JOptionPane.INFORMATION_MESSAGE);     
            SingletonProxyServidor singletonProxyServidor = SingletonProxyServidor.getInstancia();
                try {
                    //Guardar la nueva Cafeteria en su carpeta.
                    singletonProxyServidor.guardar_archivo(cafeteria);
                } catch (IOException ex) {
                    Logger.getLogger(VentanaAltaProductoCafeteria.class.getName()).log(Level.SEVERE, null, ex);
                }
                
            actualizarListaComandas();
        
        } catch (IOException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this,"\nNo existe dicha comanda " ,"Fallo",JOptionPane.ERROR_MESSAGE);            
        }


    }//GEN-LAST:event_jButtonCocinarActionPerformed

